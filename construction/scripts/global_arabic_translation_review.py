def export_missing_reviews(apps=None):
    """Export empty Arabic PO entries for review.

    Args:
        apps: optional comma-separated app list. Defaults to frappe,erpnext.
    """
    target_apps = _parse_apps(apps) or ["frappe", "erpnext"]
    results = {}
    for app in target_apps:
        results[app] = export_review(app=app, csv_path=DEFAULT_EXPORTS[app], only_missing=True)
    return results


def export_review(app, csv_path=None, only_missing=True):
    """Export Arabic PO rows for one app.

    When only_missing is True, exports only messages with empty msgstr.
    """
    _validate_app(app)
    csv_path = csv_path or DEFAULT_EXPORTS.get(app) or f"apps/construction/docs/{app}_ar_review.csv"
    rows = []

    with _po_path(app).open("rb") as po_file:
        catalog = read_po(po_file, locale="ar")

    for message in catalog:
        if not message.id:
            continue
        if only_missing and message.string:
            continue
        rows.append(
            {
                "app": app,
                "msgid": message.id,
                "msgstr": message.string or "",
                "context": message.context or "",
                "locations": "; ".join(f"{path}:{line}" for path, line in message.locations),
                "comments": " | ".join(message.auto_comments or []),
                "skip": _should_skip(message.id),
                "review_notes": "",
            }
        )

    output_path = _bench_path(csv_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    _write_csv(
        output_path,
        rows,
        ["app", "msgid", "msgstr", "context", "locations", "comments", "skip", "review_notes"],
    )
    print(f"Exported {len(rows)} rows for {app} to {output_path}")
    return {"app": app, "rows": len(rows), "path": str(output_path)}


def fill_review_from_glossary(app, csv_path=None, output_path=None, overwrite=False):
    """Fill a review CSV with reviewed glossary translations.

    This keeps machine-fill conservative: only exact reviewed glossary or existing
    DB override entries are populated, skipped rows stay skipped, and existing
    msgstr values are preserved unless overwrite is explicitly enabled.
    """
    _validate_app(app)
    csv_path = csv_path or DEFAULT_EXPORTS.get(app) or f"apps/construction/docs/{app}_ar_review.csv"
    output_path = output_path or csv_path
    rows = _read_csv(_bench_path(csv_path))
    glossary = dict(REVIEWED_AR_GLOSSARY)
    for translation in frappe.get_all(
        "Translation",
        filters={"language": "ar"},
        fields=["source_text", "translated_text"],
    ):
        if translation.source_text and translation.translated_text:
            glossary.setdefault(translation.source_text, translation.translated_text)

    filled = 0

    for row in rows:
        if (row.get("skip") or "").strip().lower() in {"1", "true", "yes", "y"}:
            continue
        if row.get("msgstr") and not overwrite:
            continue
        msgid = row.get("msgid") or ""
        msgstr = glossary.get(msgid)
        if msgstr is None:
            msgstr = glossary.get(msgid.strip())
        if not msgstr:
            continue
        row["msgstr"] = msgstr
        row["review_notes"] = row.get("review_notes") or "reviewed_glossary"
        filled += 1

    output = _bench_path(output_path)
    _write_csv(
        output,
        rows,
        ["app", "msgid", "msgstr", "context", "locations", "comments", "skip", "review_notes"],
    )
    result = {"app": app, "filled": filled, "source": str(_bench_path(csv_path)), "output": str(output)}
    print(result)
    return result


def import_review(app, csv_path, dry_run=True):
    """Import reviewed Arabic PO rows for one app.

    Rows with `skip` set to 1/true/yes are ignored.
    """
    _validate_app(app)
    input_path = _bench_path(csv_path)
    rows = _read_csv(input_path)
    reviewed = {}
    for row in rows:
        if (row.get("skip") or "").strip().lower() in {"1", "true", "yes", "y"}:
            continue
        msgid = row.get("msgid")
        msgstr = row.get("msgstr")
        if not msgid or not msgstr:
            continue
        reviewed[(msgid, row.get("context") or None)] = msgstr

    po_path = _po_path(app)
    with po_path.open("rb") as po_file:
        catalog = read_po(po_file, locale="ar")

    changed = 0
    for message in catalog:
        if not message.id:
            continue
        key = (message.id, message.context or None)
        if key in reviewed and message.string != reviewed[key]:
            message.string = reviewed[key]
            changed += 1

    if not dry_run:
        with po_path.open("wb") as po_file:
            write_po(po_file, catalog, width=120, sort_output=False, include_previous=False)

    result = {"app": app, "changed": changed, "dry_run": dry_run, "source": str(input_path)}
    print(result)
    return result


def fill_remaining_po(apps=None, dry_run=True):
    """Fill every remaining empty Arabic PO entry for the selected apps.

    This is intentionally a coverage pass. Reviewed exact translations and DB
    overrides are used first; otherwise a deterministic Arabic draft is produced
    while preserving placeholders, HTML tags, URLs, and code-like tokens.
    """
    selected_apps = _parse_apps(apps) or ["frappe", "erpnext"]
    glossary = _arabic_glossary()
    results = []

    for app in selected_apps:
        _validate_app(app)
        po_path = _po_path(app)
        with po_path.open("rb") as po_file:
            catalog = read_po(po_file, locale="ar")

        changed = 0
        for message in catalog:
            if not message.id or message.string:
                continue
            message.string = _draft_translate(message.id, glossary)
            changed += 1

        if changed and not dry_run:
            with po_path.open("wb") as po_file:
                write_po(po_file, catalog, width=120, sort_output=False, include_previous=False)

        result = {"app": app, "changed": changed, "dry_run": dry_run, "path": str(po_path)}
        print(result)
        results.append(result)

    return results


def export_remaining_reviews(apps=None):
    """Refresh missing review CSVs after PO import or draft fill."""
    return export_missing_reviews(apps=apps)


def _catalog_stats(app):
    with _po_path(app).open("rb") as po_file:
        catalog = read_po(po_file, locale="ar")

    total = translated = empty = fuzzy = 0
    for message in catalog:
        if not message.id:
            continue
        total += 1
        if message.string:
            translated += 1
        else:
            empty += 1
        if "fuzzy" in (message.flags or []):
            fuzzy += 1

    return {"app": app, "total": total, "translated": translated, "empty": empty, "fuzzy": fuzzy}


def _arabic_glossary():
    glossary = {}
    glossary.update(DRAFT_AR_EXACT)
    glossary.update(REVIEWED_AR_GLOSSARY)
    for translation in frappe.get_all(
        "Translation",
        filters={"language": "ar"},
        fields=["source_text", "translated_text"],
    ):
        if translation.source_text and translation.translated_text:
            glossary[translation.source_text] = translation.translated_text
    return glossary


def _draft_translate(text, glossary):
    if text in glossary:
        return glossary[text]
    stripped = text.strip()
    if stripped in glossary:
        return _restore_outer_space(text, glossary[stripped])
    if not stripped:
        return text
    if _is_code_like(stripped):
        return text

    protected, tokens = _protect_fragments(text)
    translated = _translate_known_patterns(protected, glossary)
    if translated == protected:
        translated = _translate_words(protected)
    translated = _restore_fragments(translated, tokens)
    return translated or text


def _translate_known_patterns(text, glossary):
    patterns = [
        (r"^Please (.+)$", "يرجى {0}"),
        (r"^Cannot (.+)$", "لا يمكن {0}"),
        (r"^Can not (.+)$", "لا يمكن {0}"),
        (r"^Unable to (.+)$", "تعذر {0}"),
        (r"^Failed to (.+)$", "فشل {0}"),
        (r"^Are you sure you want to (.+)\?$", "هل أنت متأكد أنك تريد {0}؟"),
        (r"^Do you want to (.+)\?$", "هل تريد {0}؟"),
        (r"^(.+) is required$", "{0} مطلوب"),
        (r"^(.+) is mandatory$", "{0} إلزامي"),
        (r"^(.+) is not valid$", "{0} غير صالح"),
        (r"^(.+) not found$", "لم يتم العثور على {0}"),
        (r"^(.+) already exists$", "{0} موجود بالفعل"),
        (r"^(.+) has been created$", "تم إنشاء {0}"),
        (r"^(.+) has been updated$", "تم تحديث {0}"),
        (r"^(.+) has been deleted$", "تم حذف {0}"),
        (r"^(.+) cannot be greater than (.+)$", "لا يمكن أن يكون {0} أكبر من {1}"),
        (r"^(.+) cannot be less than (.+)$", "لا يمكن أن يكون {0} أقل من {1}"),
        (r"^(.+) must be greater than (.+)$", "يجب أن يكون {0} أكبر من {1}"),
        (r"^(.+) must be less than (.+)$", "يجب أن يكون {0} أقل من {1}"),
        (r"^(.+) for (.+)$", "{0} لـ {1}"),
        (r"^(.+) of (.+)$", "{0} لـ {1}"),
        (r"^(.+) in (.+)$", "{0} في {1}"),
        (r"^(.+) against (.+)$", "{0} مقابل {1}"),
    ]
    for pattern, template in patterns:
        match = re.match(pattern, text, flags=re.IGNORECASE | re.DOTALL)
        if not match:
            continue
        parts = [_translate_words(part.strip()) for part in match.groups()]
        return template.format(*parts)
    return text


def _translate_words(text):
    if not text:
        return text
    output = text
    phrase_terms = sorted(DRAFT_AR_TERMS.items(), key=lambda item: len(item[0]), reverse=True)
    for english, arabic in phrase_terms:
        output = re.sub(rf"\b{re.escape(english)}\b", arabic, output, flags=re.IGNORECASE)
    output = _cleanup_arabic_draft(output)
    if re.search(r"[\u0600-\u06ff]", output):
        return output
    return text


def _cleanup_arabic_draft(text):
    text = re.sub(r"\s+", " ", text)
    text = text.replace(" / ", "/")
    text = text.replace(" :", ":")
    text = text.replace(" .", ".")
    text = text.replace(" ,", ",")
    text = text.replace(" ?", "?")
    text = text.replace(" !", "!")
    text = re.sub(r"\bالـ\s+", "الـ", text)  # noqa: RUF001
    return text.strip()


def _protect_fragments(text):
    tokens = []

    def replace(match):
        tokens.append(match.group(0))
        return f"__AR_TOKEN_{len(tokens) - 1}__"

    pattern = re.compile(
        r"(<[^>]+>|https?://[^\s\"'<>]+|www\.[^\s\"'<>]+|"
        r"\{[^}]*\}|%\([^)]+\)s|%[sdif]|`[^`]+`|"
        r"\b[A-Z]{2,}(?:-[A-Z0-9]+)*\b)"
    )
    return pattern.sub(replace, text), tokens


def _restore_fragments(text, tokens):
    for index, token in enumerate(tokens):
        text = text.replace(f"__AR_TOKEN_{index}__", token)
    return text


def _restore_outer_space(original, translated):
    prefix = original[: len(original) - len(original.lstrip())]
    suffix = original[len(original.rstrip()) :]
    return f"{prefix}{translated}{suffix}"


def _is_code_like(text):
    if re.fullmatch(r"[\W\d_]+", text):
        return True
    if re.fullmatch(r"[A-Z0-9_.:/-]+", text) and len(text) <= 32:
        return True
    if re.fullmatch(r"[a-z_][a-z0-9_]*(\.[a-z_][a-z0-9_]*)*", text):
        return True
    return False


def _should_skip(msgid):
    if not isinstance(msgid, str):
        return ""
    text = msgid.strip()
    if not text:
        return "yes"
    if text in {"!=", "=", ">", "<", ">=", "<=", "+", "-", "*", "/"}:
        return "yes"
    if not any(char.isalpha() for char in text):
        return "yes"
    return ""


def _parse_apps(apps):
    if not apps:
        return []
    if isinstance(apps, str):
        return [app.strip() for app in apps.split(",") if app.strip()]
    return list(apps)


def _validate_app(app):
    if app not in APP_PO_PATHS:
        frappe.throw(f"Unsupported app: {app}")


